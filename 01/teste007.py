frase1 = 'a pandemia um dia vai acabar'


print(frase1)
print(frase1[1:8])  # substring do indice 1 até o (8-1)
print(frase1[::2])  # substring do começo ao fim com passo de 2
print(frase1[::-1])  # substring do começo ao fim com passo de -1
print(frase1[3:])   # substring do indice 3 ao final
print(frase1[:11])  # substring do começo ao indice 11
print('------------------')

print(frase1[-10:-1])


